          <h1>
            Beroepsprofiel
          </h1>
          <p>
            Voor dit verslag moesten we een bericht met 3 vragen sturen naar een bedrijf waar we later zouden willen werken. <br>
            Dit bedrijf mocht van elk land zijn zolang we er maar zouden willen werken. Mijn keuze voor dit verslag was Guerrilla Games. <br>
            De reden dat ik koos voor Guerrilla was omdat ze 1 van mijn favoriete games hebben gemaakt, Horizon Zero Dawn,
            en omdat ze een nederlandse studio zijn.
            Jammer genoeg heb ik nooit antwoord terug gekregen van Guerrilla. Hierdoor heb ik 2 docenten de vragen gestuurd. <br>
            Dit zijn de antwoorden op mijn vragen.
          </p>
          <h3>Vraag:</h3>
          <p>”Wat vind je leuk aan je werk?”</p>
          <h3>Antwoord van meneer Auerbach:</h3>
          <p>”Mijn baan is erg leuk en afwisselend. Elke dag ben ik wel bezig met een nieuwe uitdaging. ”</p>
          <h3>Antwoord van meneer Springer:</h3>
          <p>”Het leukste aan mijn werk is dat je continu iets nieuws leert en iets kan creeren vanuit het niets.
           Je bent elke dag bezig met problemen oplossen en elke keer dat dat lukt heb je weer iets geleerd.
           Het enige wat je nodig hebt is een computer en je eigen creativiteit(en problemen natuurlijk.)”</p>
          <h3>Vraag:</h3>
          <p>”Wat doe je in een dag werk?”</p>
          <h3>Antwoord van meneer Auerbach:</h3>
          <p>“Niet elke dag is hetzelfde. We beginnen vaak wel met een standup en dan bekijk ik het werk van gisteren en wat we vandaag gaan doen. Er is een deel code wat opgeleverd moet worden, maar soms ben ik meer met het coachen van het team bezig, dan weer met klantgesprekken en andere dagen weer vol aan het coderen en testen.”</p>
          <h3>Antwoord van meneer Springer:</h3>
          <p>“Wat zijn de taken van een programmeur?
            De taken die ik heb is voornamelijk het front-end gedeelte van het ontwikkelen van de app. Dat is mijn hoofdtaak. Maar wij hebben ook een back-end gedeelte en daarvoor ben ik de reviewer. We werken met het vier ogen principe. Als je jou code af hebt kan je code pas doorgevoerd worden als een andere developer dat hebt ge-reviewd. Verder hij is binnen mijn bedrijf bezig met alle taken rondom het samenwerken en ben ik de technische man die de juiste tools onderzoekt en doorvoert voor het verbeteren van de samenwerking.
            Naast programmeren ben ik tot nu toe de enige designer(kennis van de design tools en principes van een goed design) in het team dus die rol pak ik er ook bij.”
          </p>
          <h3>Vraag:</h3>
          <p>“Welke eigenschappen heeft een programmeur nodig?”</p>
          <h3>Antwoord van meneer Auerbach:</h3>
          <p>“Er zijn veel belangrijke aspecten die een programmeur moet hebben, een top drie daarin is denk ik; <br>
            1) Open staan dat er meerdere oplossingen mogelijk zijn en die kunnen bespreken <br>
            2) Technieken en innovaties binnen ICT bij houden <br>
            3) Goed weten waar je goede antwoorden kan vinden als je vast zit.”
          </p>
          <h3>Antwoord van meneer Springer:</h3>
          <p>“Wat is nodig om een geschikte programmeur te worden?
            Doorzettingsvermogen en niet opgeven als je vast zit. Verder moet je een expert Google-laar wezen. Je moet niet alleen weten hoe je moet zoeken maar ook weten of wat je ziet bruikbaar is in jou situatie. Verder moet je kunnen accepteren dat je fouten maakt en dat dat niet erg is. Van je fouten leer je juist ontzettend veel en als er 1 vakgebied is waar je fouten je extra informatie geven is het wel programmeren.”
          </p>
          <br> 
          <p>Voor dat ik aan dit verslag begon dacht ik dat een programmeur elke dag doorwerkte aan een gedeelte van een project over een bepaalde hoeveelheid tijd. Ook dacht ik dat de eigenschappen die voor een programmeur nodig waren is dat hij/zij creatieve oplossingen kan bedenken op problemen en creatief moet kunnen nadenken om een programma te maken.</p>
          <p>Volgens het internet klopte mijn idee van wat een programmeur per dag doet, maar was mijn idee van wat de eigenschappen die een programmeur nodig heeft maar een klein deel van het geheel.</p>
          <h3>In conclusie:</h3>
          <p>Een programmeur heeft als taken dat hij/zij per dag het werk doet dat nodig is voor het project, dit kan werk zijn dat is overgebleven van een vorige dag of vooruit werken. <br>
            Een goede programmeur weet dat het niet erg is als hij vastloopt en dat hij/zij weet hoe die hulp moet zoeken als hij/zij vastloopt.</p>
